package twoConditions;

public class If_Else {

	public static void main(String[] args) {
		
		//One Condition
		
		int x = 20;
		
		if(x>=20) {
			
			System.out.println("Test pass");
			
			
		}
		
		//Two Condition's (else)
		
		
				int y = 20;
				
				if(y>=40) {
					
					System.out.println("Test pass");
					
			}else {
				
				System.out.println("Test fail");
				
				
			}
		
		

	}

}
